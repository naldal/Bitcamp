package member.bean;

public class ImageBoardDTO {

	private int seq;
	private String imageID;
	private String imageName;
	private String imagePrice;
	private String imageQty;
	private String imageContent;
	private String image1;
	private String logtime;
	
	public int getSeq() {
		return seq;
	}
	public void setSeq(int seq) {
		this.seq = seq;
	}
	public String getImageID() {
		return imageID;
	}
	public void setImageID(String imageID) {
		this.imageID = imageID;
	}
	public String getImageName() {
		return imageName;
	}
	public void setImageName(String imageName) {
		this.imageName = imageName;
	}
	public String getImagePrice() {
		return imagePrice;
	}
	public void setImagePrice(String imagePrice) {
		this.imagePrice = imagePrice;
	}
	public String getImageQty() {
		return imageQty;
	}
	public void setImageQty(String imageQty) {
		this.imageQty = imageQty;
	}
	public String getImageContent() {
		return imageContent;
	}
	public void setImageContent(String imageContent) {
		this.imageContent = imageContent;
	}
	public String getImage1() {
		return image1;
	}
	public void setImage1(String image1) {
		this.image1 = image1;
	}
	public String getLogtime() {
		return logtime;
	}
	public void setLogtime(String logtime) {
		this.logtime = logtime;
	}
	
	
	
}
